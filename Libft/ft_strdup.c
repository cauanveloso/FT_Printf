#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*dst;

	dst = malloc(sizeof(char) * (ft_strlen((char *)s) + 1));
	if (!(dst))
	{
		return (NULL);
	}
	i = 0;
	while (s[i])
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
